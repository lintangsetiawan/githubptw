    <h1>Framework PHP</h1>
    <p>
    Framework atau dalam bahasa Indonesia yaitu kerangka kerja dapat diartikan sebagai kumpulan 
    dari library (Class) yang bisa diturunkan,
    <br>atau bisa langsung dipakai fungsinya oleh modul-modul 
    atau fungsi yang akan kita kembangkan (Septian, 2011:7). Framework merupakan<br>sekumpulan fungsi, 
    class dan aturan yang bersifat menyeluruh mengatur bagaimana kita membangun aplikasi. Framework 
    memungkinkan<br>untuk membangun sebuah aplikasi dengan lebih cepat. Karena pengembang hanya akan 
    memfokuskan pada pokok permasalahan yang<br>diminta untuk sebuah aplikasi, sedangkan hal-hal 
    penunjang lainnya seperti koneksi database, form validation, GUI dan security, umumnya<br>telah 
    disediakan oleh framework.
    <br><br>
    Kelebihan dalam mengembangkan aplikasi dengan menggunakan framework yaitu:
    <br><br>
    1. Akan lebih mempermudah dalam memahami mekanisme kerja sebuah aplikasi. Hal tersebut tentunya akan membantu 
    proses pengembangan<br>yang dilakukan secara tim.<br>
    2. Dengan memakai framework akan menghemat waktu pengerjaan suatu aplikasi, karena setiap anggota sudah 
    memiliki sebuah acuan dalam<br>menyelesaikan modul. Dalam hal ini misalnya semakin banyak library yang ada 
    maka akan semakin cepat anggota tim dalam menemukan solusi<br>karena tidak setiap anggota harus membuat class 
    atau fungsi untuk kasus yang relatif sama.
    <br><br>
    Framework PHP dalah sebuah lingkungan pengembangan aplikasi dengan menggunakan bahasa pemrograman PHP yang 
    berisi sejumlah class<br>yang telah dibuat agar dapat digunakan kembali dalam membuat aplikasi. Berbagai jenis 
    class dalam framework kemungkinan besar berbeda<br>antara framework yang satu dengan yang lainnya sesuai dengan 
    tujuan dari framework tersebut. Sebagai besar Framework PHP yang tersedia<br>saat ini berbasis MVC (model view 
    controller). Ini adalah framework yang modern dimana MVC adalah sebuah pendekatan perangkat lunak<br>yang 
    memisahkan aplikasi logika dari presentasi. Ini akan meminimalkan script dari halaman-halaman web dimana 
    scrip presentasi (HTML,<br>CSS dan JavaScript) dipisahkan dari PHP Scripting. Hal tersebut dilakukan untuk 
    menghindari spaghetti code. Berikut adalah beberapa framework<br>PHP yang banyak digunakan oleh para pengembang 
    web pada saat ini:
    <br><br>
    1. CodeIgniter adalah sebuah framework aplikasi untuk membuat halaman atau aplikasi web berbasis PHP. Selain 
    mudah digunakan, Code Igniter<br>juga sangat cepat dan ringan. Dokumentasinya yang lengkap membuat framework ini 
    mudah untuk dipelajari oleh pemula.<br>
    2. Yii adalah kerangka kerja PHP berbasis-komponen dengan performansi tinggi untuk pengembangan aplikasi web 
    berskala-besar. Ia menyediakan<br>resuabilitas maksimum dalam pemrograman web dan bisa mengakselerasi proses 
    pengembangan secara signifikan kelebihan Yii melampaui framework<br>PHP lain dalam hal efisiensi, kaya-fitur, 
    dan didokumentasikan dengan jelas. Yii didesain dengan hati-hati dari awal agar sesuai untuk pengembangan<br>
    aplikasi web secara serius.<br>
    3. Zend Framework adalah framework open source dan berkualitas tinggi untuk membangun aplikasi web dan web 
    service dengan PHP
    </p>
